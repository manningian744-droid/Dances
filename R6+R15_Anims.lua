-- Get humanoid function
local function GetHumanoid()
    local char = Player.Character
    if char then
        return char:FindFirstChild("Humanoid")
    end
    return nil
end

-- Play animation function
local function PlayAnimation(animationId)
    local humanoid = GetHumanoid()
    if not humanoid then
        Rayfield:Notify({
            Title = "Error",
            Content = "Humanoid not found!",
            Duration = 3,
        })
        return
    end
    
    local success, err = pcall(function()
        local anim = Instance.new("Animation")
        anim.AnimationId = "rbxassetid://" .. tostring(animationId)
        local animator = humanoid:FindFirstChildOfClass("Animator")
        if not animator then
            animator = Instance.new("Animator")
            animator.Parent = humanoid
        end
        local track = animator:LoadAnimation(anim)
        track:Play()
    end)
    
    if not success then
        Rayfield:Notify({
            Title = "Error",
            Content = "Failed to play animation",
            Duration = 2,
        })
    end
end

-- Create the GUI
local function CreateAnimationGUI()
    -- Create the main window
    local Window = Rayfield:CreateWindow({
        Name = "R6 Animation Loader",
        LoadingTitle = "Loading R6 Animations...",
        LoadingSubtitle = "Organizing into categories...",
        ConfigurationSaving = {
            Enabled = true,
            FolderName = "R6AnimLoader",
            FileName = "R6AnimLoader"
        },
        KeySystem = false,
    })
    
    -- Create tabs for each category
    local tabs = {}
    local tabIcons = {
        Movement = 4483362458,
        Combat = 4483362458,
        Ranged = 4483362458,
        Weapons_Equipment = 4483362458,
        Items_Tools = 4483362458,
        Dance_Emotes = 4483362458,
        Vehicles = 4483362458,
        Weapon_Special = 4483362458,
    }
    
    local totalAnimations = 0
    local totalTabs = 0
    local animationButtons = {}
    
    -- Create tabs and store all animations for search
    for categoryName, animations in pairs(R6Animations) do
        local displayName = categoryName:gsub("_", " ")
        local tab = Window:CreateTab(displayName, tabIcons[categoryName] or 4483362458)
        tabs[categoryName] = tab
        totalTabs = totalTabs + 1
        
        -- Create sections within the tab
        local section = tab:CreateSection(displayName)
        local buttonCount = 0
        
        -- Sort animations by name
        local sorted = {}
        for id, name in pairs(animations) do
            table.insert(sorted, {id = id, name = name})
        end
        table.sort(sorted, function(a, b)
            return a.name < b.name
        end)
        
        -- Create buttons
        for _, anim in ipairs(sorted) do
            -- Create a new section every 60 buttons
            if buttonCount % 60 == 0 and buttonCount > 0 then
                section = tab:CreateSection(displayName .. " (" .. (buttonCount + 1) .. "+)")
            end
            
            tab:CreateButton({
                Name = anim.name,
                Callback = function()
                    PlayAnimation(anim.id)
                end,
            })
            
            table.insert(animationButtons, {name = anim.name, id = anim.id, category = categoryName})
            buttonCount = buttonCount + 1
            totalAnimations = totalAnimations + 1
        end
    end
    
    -- Create Search Tab
    local SearchTab = Window:CreateTab("Search", 4483362458)
    local SearchSection = SearchTab:CreateSection("Search Animations")
    
    local lastSearchText = ""

    SearchTab:CreateInput({
        Name = "Search",
        PlaceholderText = "Type animation name...",
        Callback = function(text)
            -- Avoid redundant searches
            if text == lastSearchText then return end
            lastSearchText = text

            if text == "" then
                SearchTab:CreateSection("Type a name above to search animations")
                return
            end

            local found = 0
            local searchText = string.lower(text)
            local results = {}

            for _, anim in ipairs(animationButtons) do
                if string.find(string.lower(anim.name), searchText, 1, true) then
                    table.insert(results, anim)
                    found = found + 1
                    if found >= 50 then break end -- cap results to avoid flooding the UI
                end
            end

            if found == 0 then
                SearchTab:CreateSection("No matches found for: " .. text)
            else
                SearchTab:CreateSection("Found " .. found .. " result(s) for: " .. text)
                for _, anim in ipairs(results) do
                    SearchTab:CreateButton({
                        Name = anim.name .. " (" .. anim.category:gsub("_", " ") .. ")",
                        Callback = function()
                            PlayAnimation(anim.id)
                        end,
                    })
                end
            end
        end
    })
    
    -- Create Controls Tab
    local ControlsTab = Window:CreateTab("Controls", 4483362458)
    local ControlsSection = ControlsTab:CreateSection("Controls")
    
    ControlsTab:CreateButton({
        Name = "Stop All Animations",
        Callback = function()
            local humanoid = GetHumanoid()
            if humanoid then
                local animator = humanoid:FindFirstChild("Animator")
                if animator then
                    for _, track in ipairs(animator:GetPlayingAnimationTracks()) do
                        track:Stop()
                    end
                end
                Rayfield:Notify({
                    Title = "Stopped",
                    Content = "All animations stopped",
                    Duration = 2,
                })
            end
        end,
    })
    
    ControlsTab:CreateButton({
        Name = "Reset Character",
        Callback = function()
            local humanoid = GetHumanoid()
            if humanoid then
                humanoid:BreakJoints()
            end
        end,
    })
    
    ControlsTab:CreateButton({
        Name = "Reload GUI",
        Callback = function()
            Rayfield:Destroy()
            task.wait(0.5)
            CreateAnimationGUI()
        end,
    })
    
    -- Info
    ControlsTab:CreateLabel("Total R6 Animations: " .. totalAnimations)
    ControlsTab:CreateLabel("Categories: " .. totalTabs)

    Rayfield:Notify({
        Title = "Loaded",
        Content = totalAnimations .. " R6 animations organized into " .. totalTabs .. " tabs!",
        Duration = 3,
    })
end

-- Main execution
local success, err = pcall(CreateAnimationGUI)
if not success then
    warn("Error: " .. tostring(err))
    Rayfield:Notify({
        Title = "Error",
        Content = "Failed to load GUI: " .. tostring(err),
        Duration = 5,
    })
end
